// Parse command line arguments
int parse_arguments(int argc, char* argv[], dispatcher_args_t* args) {
    memset(args, 0, sizeof(dispatcher_args_t));

    for (int i = 1; i < argc; i++) {
        if (strcmp(argv[i], "--help") == 0 || strcmp(argv[i], "-h") == 0) {
            print_usage(argv[0]);
            return 1; // Exit after showing help
        }
        else if (strcmp(argv[i], "--type") == 0 && i + 1 < argc) {
            args->type = argv[++i];
        }
        else if (strcmp(argv[i], "--output-dir") == 0 && i + 1 < argc) {
            args->output_dir = argv[++i];
        }
        else if (strcmp(argv[i], "--chains") == 0 && i + 1 < argc) {
            args->chains = split_string(argv[++i], &args->chains_count);
        }
        else if (strcmp(argv[i], "--slots") == 0 && i + 1 < argc) {
            args->slots = split_string(argv[++i], &args->slots_count);
        }
        else if (strcmp(argv[i], "--events") == 0 && i + 1 < argc) {
            args->events = split_string(argv[++i], &args->events_count);
        }
    }

    // Set defaults
    if (!args->type) {
        args->type = "main";
    }
    if (!args->output_dir) {
        args->output_dir = ".";
    }

    return 0;
}

// Cleanup function for arguments
void cleanup_arguments(dispatcher_args_t* args) {
    if (args->chains) {
        free_string_array(args->chains);
    }
    if (args->slots) {
        free_string_array(args->slots);
    }
    if (args->events) {
        free_string_array(args->events);
    }
}

int main(int argc, char* argv[]) {
    dispatcher_args_t args;

    // Parse command line arguments
    int result = parse_arguments(argc, argv, &args);
    if (result == 1) {
        return 0; // Help was shown, exit normally
    }
    if (result != 0) {
        fprintf(stderr, "Error parsing arguments\n");
        return 1;
    }

    // Validate required arguments
    if (!args.type) {
        fprintf(stderr, "Error: --type is required\n");
        print_usage(argv[0]);
        cleanup_arguments(&args);
        return 1;
    }

    // Generate appropriate dispatcher based on type
    int success = 0;
    if (strcmp(args.type, "main") == 0) {
        success = generate_main_dispatcher(args.output_dir);
    }
    else if (strcmp(args.type, "chain") == 0) {
        success = generate_chain_dispatcher(args.output_dir);
    }
    else {
        fprintf(stderr, "Error: Unknown type '%s'. Use 'main' or 'chain'\n", args.type);
        print_usage(argv[0]);
        cleanup_arguments(&args);
        return 1;
    }

    if (success == 0) {
        printf("Successfully generated dispatcher templates in %s\n", args.output_dir);
    } else {
        fprintf(stderr, "Failed to generate dispatcher templates\n");
        success = 1;
    }

    cleanup_arguments(&args);
    return success;
}