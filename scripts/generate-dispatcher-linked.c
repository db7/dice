// Simple argument parsing structure
typedef struct {
    char* type;
    char* output_dir;
    char** chains;
    char** slots;
    char** events;
    int chains_count;
    int slots_count;
    int events_count;
} dispatcher_args_t;

// Function to print usage information
void print_usage(const char* prog_name) {
    printf("Usage: %s [OPTIONS]\n", prog_name);
    printf("\nOptions:\n");
    printf("  --type TYPE          Type of dispatcher template to generate (main|chain)\n");
    printf("  --output-dir DIR     Output directory for generated files\n");
    printf("  --chains LIST        Comma-separated list of chain IDs\n");
    printf("  --slots LIST         Comma-separated list of slot IDs\n");
    printf("  --events LIST        Comma-separated list of event IDs\n");
    printf("  --help               Show this help message\n");
    printf("\nExamples:\n");
    printf("  %s --type main\n", prog_name);
    printf("  %s --type chain --output-dir ./generated\n", prog_name);
    printf("  %s --type main --chains 0,1,2 --slots 0,1,2,3\n", prog_name);
}

// Helper function to split comma-separated strings
char** split_string(const char* str, int* count) {
    if (!str) {
        *count = 0;
        return NULL;
    }

    // Count commas to determine array size
    int num_items = 1;
    const char* ptr = str;
    while (*ptr) {
        if (*ptr == ',') num_items++;
        ptr++;
    }

    char** result = malloc((num_items + 1) * sizeof(char*));
    if (!result) return NULL;

    *count = num_items;

    char* temp = strdup(str);
    if (!temp) {
        free(result);
        return NULL;
    }

    char* token = strtok(temp, ",");
    int i = 0;
    while (token && i < num_items) {
        result[i] = strdup(token);
        // Trim whitespace
        while (*result[i] == ' ') result[i]++;
        char* end = result[i] + strlen(result[i]) - 1;
        while (end > result[i] && *end == ' ') *end-- = '\0';
        i++;
        token = strtok(NULL, ",");
    }
    result[i] = NULL;

    free(temp);
    return result;
}

// Helper function to free string array
void free_string_array(char** arr) {
    if (!arr) return;
    for (int i = 0; arr[i]; i++) {
        free(arr[i]);
    }
    free(arr);
}

// Function to create directory if it doesn't exist
int create_directory(const char* path) {
    struct stat st = {0};
    if (stat(path, &st) == -1) {
        return mkdir(path, 0755);
    }
    return 0;
}

