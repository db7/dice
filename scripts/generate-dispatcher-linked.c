/*
 * Dice Dispatcher Generator - Direct libtmplr Linking Version
 *
 * This tool generates dispatcher templates for Dice modules by directly linking
 * with the libtmplr library instead of invoking the tmplr executable.
 * It provides a more efficient and integrated approach compared to the shell-based approach.
 */

#include <stdio.h>
#include <stdlib.h>
#include <string.h>
#include <unistd.h>
