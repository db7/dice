/*
 * Dice Fine-Grained Dispatcher Generator
 *
 * This tool generates dispatcher templates for Dice modules with fine-grained control.
 * It supports generating:
 * 1. One file per chain that dispatches to a set of events
 * 2. One file per chain/event that dispatches to a set of slots
 *
 * This allows different chain-event-slot combinations to be configured independently.
 */

#include <stdio.h>
#include <stdlib.h>
#include <string.h>
#include <unistd.h>
#include <dirent.h>
#include <sys/stat.h>
#include <ctype.h>

// Structure to hold chain-event-slot configurations
typedef struct {
    char* chain;
    char* event;
    char* slot;
} chain_event_slot_t;

// Structure for configuration
typedef struct {
    char* type;  // main, chain, chain-event, slot
    char* output_dir;
    char** chains;
    char** events;
    char** slots;
    int chains_count;
    int events_count;
    int slots_count;
} dispatcher_config_t;

// Function to print usage information
void print_usage(const char* prog_name) {
    printf("Usage: %s [OPTIONS]\n", prog_name);
    printf("\nOptions:\n");
    printf("  --type TYPE          Type of dispatcher template to generate (main|chain|chain-event|slot)\n");
    printf("  --output-dir DIR     Output directory for generated files\n");
    printf("  --chains LIST        Comma-separated list of chain IDs\n");
    printf("  --events LIST        Comma-separated list of event IDs\n");
    printf("  --slots LIST         Comma-separated list of slot IDs\n");
    printf("  --help               Show this help message\n");
    printf("\nExamples:\n");
    printf("  %s --type main\n", prog_name);
    printf("  %s --type chain --chains 0,1,2\n", prog_name);
    printf("  %s --type chain-event --chains 0,1 --events 0,1,2\n", prog_name);
    printf("  %s --type slot --slots 0,1,2\n", prog_name);
}

// Helper function to split comma-separated strings
char** split_string(const char* str, int* count) {
    if (!str) {
        *count = 0;
        return NULL;
    }

    // Count commas to determine array size
    int num_items = 1;
    const char* ptr = str;
    while (*ptr) {
        if (*ptr == ',') num_items++;
        ptr++;
    }

    char** result = malloc((num_items + 1) * sizeof(char*));
    if (!result) return NULL;

    *count = num_items;

    char* temp = strdup(str);
    if (!temp) {
        free(result);
        return NULL;
    }

    char* token = strtok(temp, ",");
    int i = 0;
    while (token && i < num_items) {
        result[i] = strdup(token);
        // Trim whitespace
        while (*result[i] == ' ') result[i]++;
        char* end = result[i] + strlen(result[i]) - 1;
        while (end > result[i] && *end == ' ') *end-- = '\0';
        i++;
        token = strtok(NULL, ",");
    }
    result[i] = NULL;

    free(temp);
    return result;
}

// Helper function to free string array
void free_string_array(char** arr) {
    if (!arr) return;
    for (int i = 0; arr[i]; i++) {
        free(arr[i]);
    }
    free(arr);
}

// Function to create directory if it doesn't exist
int create_directory(const char* path) {
    struct stat st = {0};
    if (stat(path, &st) == -1) {
        return mkdir(path, 0755);
    }
    return 0;
}

// Function to sanitize filename (remove invalid characters)
void sanitize_filename(char* filename) {
    for (int i = 0; filename[i]; i++) {
        if (filename[i] == '/' || filename[i] == '\\' || filename[i] == ':' ||
            filename[i] == '*' || filename[i] == '?' || filename[i] == '"' ||
            filename[i] == '<' || filename[i] == '>' || filename[i] == '|') {
            filename[i] = '_';
        }
    }
}

