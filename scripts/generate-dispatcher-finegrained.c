// Generate one file per chain
int generate_per_chain(const char* output_dir, char** chains, int chains_count) {
    printf("Generating dispatcher files per chain...\n");

    for (int i = 0; i < chains_count; i++) {
        char filename[1024];
        char sanitized_chain[256];
        strncpy(sanitized_chain, chains[i], sizeof(sanitized_chain) - 1);
        sanitized_chain[sizeof(sanitized_chain) - 1] = '\0';
        sanitize_filename(sanitized_chain);

        snprintf(filename, sizeof(filename), "%s/dispatch_chain_%s.c", output_dir, sanitized_chain);

        printf("  Generating: %s\n", filename);

        // For demonstration, we'll just create a simple file
        FILE* f = fopen(filename, "w");
        if (f) {
            fprintf(f, "/* Chain dispatcher for %s */\n", chains[i]);
            fprintf(f, "#include <dice/pubsub.h>\n\n");
            fprintf(f, "/* Generated dispatcher for chain %s */\n", chains[i]);
            fprintf(f, "DICE_HIDE enum ps_err ps_dispatch_chain_%s(const chain_id chain, const type_id type, void *event, struct metadata *md) {\n", chains[i]);
            fprintf(f, "    return PS_HANDLER_OFF;\n");
            fprintf(f, "}\n");
            fclose(f);
        }
    }

    return 0;
}

// Generate one file per chain/event
int generate_per_chain_event(const char* output_dir, char** chains, int chains_count,
                            char** events, int events_count) {
    printf("Generating dispatcher files per chain/event...\n");

    for (int i = 0; i < chains_count; i++) {
        for (int j = 0; j < events_count; j++) {
            char filename[1024];
            char sanitized_chain[256];
            char sanitized_event[256];
            strncpy(sanitized_chain, chains[i], sizeof(sanitized_chain) - 1);
            sanitized_chain[sizeof(sanitized_chain) - 1] = '\0';
            sanitize_filename(sanitized_chain);

            strncpy(sanitized_event, events[j], sizeof(sanitized_event) - 1);
            sanitized_event[sizeof(sanitized_event) - 1] = '\0';
            sanitize_filename(sanitized_event);

            snprintf(filename, sizeof(filename), "%s/dispatch_chain_%s_event_%s.c",
                    output_dir, sanitized_chain, sanitized_event);

            printf("  Generating: %s\n", filename);

            // For demonstration, we'll just create a simple file
            FILE* f = fopen(filename, "w");
            if (f) {
                fprintf(f, "/* Chain-event dispatcher for %s/%s */\n", chains[i], events[j]);
                fprintf(f, "#include <dice/pubsub.h>\n\n");
                fprintf(f, "/* Generated dispatcher for chain %s, event %s */\n", chains[i], events[j]);
                fprintf(f, "DICE_HIDE enum ps_err ps_dispatch_chain_%s_event_%s(const chain_id chain, const type_id type, void *event, struct metadata *md) {\n",
                        chains[i], events[j]);
                fprintf(f, "    return PS_HANDLER_OFF;\n");
                fprintf(f, "}\n");
                fclose(f);
            }
        }
    }

    return 0;
}

