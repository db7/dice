// Parse command line arguments
int parse_arguments(int argc, char* argv[], dispatcher_config_t* config) {
    memset(config, 0, sizeof(dispatcher_config_t));

    for (int i = 1; i < argc; i++) {
        if (strcmp(argv[i], "--help") == 0 || strcmp(argv[i], "-h") == 0) {
            print_usage(argv[0]);
            return 1; // Exit after showing help
        }
        else if (strcmp(argv[i], "--type") == 0 && i + 1 < argc) {
            config->type = argv[++i];
        }
        else if (strcmp(argv[i], "--output-dir") == 0 && i + 1 < argc) {
            config->output_dir = argv[++i];
        }
        else if (strcmp(argv[i], "--chains") == 0 && i + 1 < argc) {
            config->chains = split_string(argv[++i], &config->chains_count);
        }
        else if (strcmp(argv[i], "--events") == 0 && i + 1 < argc) {
            config->events = split_string(argv[++i], &config->events_count);
        }
        else if (strcmp(argv[i], "--slots") == 0 && i + 1 < argc) {
            config->slots = split_string(argv[++i], &config->slots_count);
        }
    }

    // Set default output directory
    if (!config->output_dir) {
        config->output_dir = ".";
    }

    // Set default type if not specified
    if (!config->type) {
        config->type = "main";
    }

    return 0;
}

// Cleanup function for arguments
void cleanup_config(dispatcher_config_t* config) {
    if (config->chains) {
        free_string_array(config->chains);
    }
    if (config->events) {
        free_string_array(config->events);
    }
    if (config->slots) {
        free_string_array(config->slots);
    }
}

int main(int argc, char* argv[]) {
    dispatcher_config_t config;

    // Parse command line arguments
    int result = parse_arguments(argc, argv, &config);
    if (result == 1) {
        return 0; // Help was shown, exit normally
    }
    if (result != 0) {
        fprintf(stderr, "Error parsing arguments\n");
        return 1;
    }

    // Create output directory if needed
    create_directory(config.output_dir);

    // Validate required arguments based on type
    if (strcmp(config.type, "chain") == 0 && !config.chains) {
        fprintf(stderr, "Error: --chains required for --type chain\n");
        cleanup_config(&config);
        return 1;
    }

    if (strcmp(config.type, "chain-event") == 0 && (!config.chains || !config.events)) {
        fprintf(stderr, "Error: --chains and --events required for --type chain-event\n");
        cleanup_config(&config);
        return 1;
    }

    if (strcmp(config.type, "slot") == 0 && !config.slots) {
        fprintf(stderr, "Error: --slots required for --type slot\n");
        cleanup_config(&config);
        return 1;
    }

    // Generate based on type
    int success = 0;

    if (strcmp(config.type, "main") == 0) {
        printf("Generating main dispatcher template...\n");
        // For main dispatcher, we'd generate the standard dispatch.c template
        // This is a simplified demonstration
        printf("  Main dispatcher generation (simplified)\n");
        success = 0;
    }
    else if (strcmp(config.type, "chain") == 0 && config.chains) {
        success = generate_per_chain(config.output_dir, config.chains, config.chains_count);
    }
    else if (strcmp(config.type, "chain-event") == 0 && config.chains && config.events) {
        success = generate_per_chain_event(config.output_dir, config.chains, config.chains_count,
                                          config.events, config.events_count);
    }
    else if (strcmp(config.type, "slot") == 0 && config.slots) {
        success = generate_per_slot(config.output_dir, config.slots, config.slots_count);
    }
    else {
        fprintf(stderr, "Error: Invalid type '%s'. Use 'main', 'chain', 'chain-event', or 'slot'\n", config.type);
        cleanup_config(&config);
        return 1;
    }

    if (success == 0) {
        printf("Successfully generated dispatcher templates in %s\n", config.output_dir);
    } else {
        fprintf(stderr, "Failed to generate dispatcher templates\n");
        success = 1;
    }

    cleanup_config(&config);
    return success;
}